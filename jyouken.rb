# 数値の比較
# 条件が正しければtrueを、誤っていればfalseを返す
p 2 == 2
p 1 == 2
# ==はこれはあってるよね？
p 3 > 1
p 3 > 3
p 3 < 1
p 3 < 3
# >、<は口が開いてる方が大きいよね？
p 3 >= 3
p 4 >= 3
